def area(a, b, c):
    '''Функция принимает значения - стороны треугольника'''
    return (a + b + c) / 2
    '''Функция возвращает площадь треугольника'''

def perimeter(a, b, c):
    '''Функция принимает значения - стороны треугольника'''
    return a + b + c
    '''Функция возвращает периметр треугольника'''